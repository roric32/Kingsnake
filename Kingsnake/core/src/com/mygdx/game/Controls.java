public class Controls {

    public int UpKey;
    public int DownKey;
    public int LeftKey;
    public int RightKey;

    public Controls(int Up, int Down, int Left, int Right) {
        this.UpKey = Up;
        this.DownKey = Down;
        this.LeftKey = Left;
        this.RightKey = Right;
    }

}
